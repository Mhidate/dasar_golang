package main

import "fmt"

func main() {
	fmt.Println("Belajar operasi perbandingan")

	a := 10
	b := 20

	fmt.Println("Apakah a == b?", a == b)
	fmt.Println("Apakah a != b?", a != b)
	fmt.Println("Apakah a > b?", a > b)
	fmt.Println("Apakah a < b?", a < b)
	fmt.Println("Apakah a < b?", a <= b)
	fmt.Println("Apakah a < b?", a >= b)

}
